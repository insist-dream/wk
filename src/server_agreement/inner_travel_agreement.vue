    <h3>第五条 旅游费用</h3>
    <p>1、旅游费用包括：（1）交通费；（2）住宿费；（3）餐费（不含酒水费）；（4）旅游景区的第一道门票费；（5）旅行社服务费；（6）导游服务费；（7）其他旅游项目费用。</p>
    <p>2、旅游费用不包括：（1）旅游者投保的个人旅游保险费用；（2）合同未约定由旅行社支付的费用；（3）行程中发生的旅游者个人费用。</p>

    <h3>第六条 付款方式</h3>
    <p>1、签订合同时，甲方应向乙方支付全额旅游费用。</p>
    <p>2、支付旅游费用时，乙方应向甲方出具发票。</p>

    <h3>第七条 旅游安全保障 </h3>
    <p>1、乙方必须购买云南旅游组合保险，并保证所提供的接待服务达到相关旅游安全标准要求。</p>
    <p>2、乙方应当提示甲方购买旅游人身意外保险及相关保险；甲方在旅游期间因自身身体状况或者其他意外因素发生人身伤害或财产损失的，按照已购买保险的相关条款处理。</p>

    <h3>第八条 合同变更</h3>
    <p>1、旅游行程开始前，甲方可将合同中自身的权利义务转让给第三人，乙方非因正当理由不得拒绝，因此增加的费用由甲方和第三人承担。</p>
    <p>2、甲乙双方协商一致，可以书面形式变更合同约定的内容，并由双方签字、盖章确认，或由甲方与乙方委派的导游或经理、计调人员签字确认；由此增加的旅游费用及给对方造成的损失，由变更提出方承担；减少的旅游费用，乙方应当退还甲方。</p>
    <p>3、在出行前遇到不可抗力（指因自然原因和社会原因引起的，如自然灾害、战争、恐怖活动、动乱、骚乱、罢工、突发公共事件、政府行为等不能预见、不能避免并不能克服的客观情况），双方经协商可以取消行程或者延期出行。取消行程的，乙方向甲方退还旅游费用；乙方能够证明已向地接社或者履行辅助人支付且不可退还的费用的，乙方可部分或者全部免除责任。</p>
    <p>4、在行程中因不可抗力导致无法履行或者继续履行合同的，乙方可以在征得甲方50%以上成员同意后，对相应内容予以变更；因情况紧急无法征求意见时，乙方可以决定内容的变更，但应当就作出的决定承担举证责任。</p>
    <p>5、因不可抗力或特殊原因需更换导游人员的，不得降低导游星级标准，且需及时告知甲方。</p>

    <h3>第九条 合同解除 </h3>
    <p>1、甲方在行程开始前7日以上（含7日）提出解除合同的，乙方应当向甲方退还全额旅游费用；甲方在行程开始前7日内和行程中解除合同的，按下列标准扣除必要的费用：</p>
    <p class="red">（1）行程开始前6日（含6日，下同）至4日，按旅游费用总额的20%。</p>
    <p class="red">（2）行程开始前3日至1日，按旅游费用总额的40%。</p>
    <p class="red">（3）行程开始当日，按旅游费用总额的60%。</p>
    <p class="red">（4）在行程中解除合同的，必要的费用扣除标准为：</p>
    <p class="red">旅游费用×60%+（旅游费用-旅游费用×60%)÷旅游天数×已经出游的天数。</p>
    <p class="red">如乙方按上述约定比例扣除的必要的费用低于实际发生的费用，甲方应按照乙方实际发生的费用支付，但最高额不应超过旅游费用总额。乙方扣除必要的费用后，应在解除合同通知到达日起5个工作日内为甲方办结退款手续。</p>
    <p class="red">2、乙方在行程开始前7日以上（含7日）提出解除合同的，应向甲方退还全额旅游费用；乙方在行程开始前7日以内提出解除合同的，应向甲方退还已收取的全部旅游费用，并按下列标准向甲方承担违约责任：</p>
    <p class="red">（1）出发前6日（含6日，下同）至4日，支付旅游费用总额10%的违约金。</p>
    <p class="red">（2）出发前3日至1日，支付旅游费用总额15%的违约金。</p>
    <p class="red">（3）出发当日，支付旅游费用总额20%的违约金。</p>
    <p class="red">如按上述比例支付的违约金不足以赔偿甲方的实际损失，乙方应当按实际损失对甲方予以赔偿，并在解除合同通知到达日起5个工作日内为甲方办结退款手续。</p>
    <p>3、甲方有下列情形之一的，乙方可以解除合同：</p>
    <p>（1）患有传染病等疾病，可能危害其他旅游者健康和安全的。</p>
    <p>（2）携带危害公共安全的物品且不同意交有关部门处理的。</p>
    <p>（3）从事违法或者违反社会公德的活动的。</p>
    <p>（4）从事严重影响其他旅游者权益的活动，且不听劝阻、不能制止的。</p>
    <p>（5）法律规定的其他情形。</p>
    <p>因前款情形解除合同的，乙方应在扣除必要的费用后，将余款退还甲方；给乙方造成损失的，甲方应当依法承担赔偿责任。</p>
    <p>4、因不可抗力或者乙方、履行辅助人已尽合理注意义务仍不能避免的事件，影响旅游行程的，按照下列情形处理：</p>
    <p>（1）合同不能继续履行的，甲乙双方均可解除合同。合同不能完全履行的，乙方向甲方作出说明，甲方同意变更的，可以在合理范围内变更合同；甲方不同意变更的，可以解除合同。</p>
    <p>（2）合同解除的，乙方应当在扣除已向地接社或者履行辅助人支付且不可退还的费用后，将余款退还甲方；合同变更的，因此增加的费用由甲方承担，减少的费用退还甲方。</p>
    <p>（3）危及甲方人身、财产安全的，乙方应采取相应的安全措施，因此支出的费用，<span class="red">由甲乙双方分担。</span></p>
    <p>（4）造成甲方滞留的，乙方应当采取相应的安置措施。因此增加的食宿费用，由甲方承担；增加的返程费用，由甲乙双方分担。</p>
    <p>5、旅游行程中解除合同的，乙方应当协助甲方返回出发地或者甲方指定的合理地点，费用由甲方承担。由于乙方或者履行辅助人的原因导致合同解除的，返程费用由乙方承担。</p>
    <h3>第十条 违约责任</h3>
    <p>1、甲方违约责任</p>
    <p>（1）甲方因不听从<span>乙方</span>及其导游的劝告、提示而影响团队行程，给 <span>乙方</span>造成损失的，应当承担相应的赔偿责任。</p>
    <p>（2）甲方超出本合同约定的内容进行个人活动所造成的损失，由其自行承担；在旅游期间甲方擅自脱团的，乙方与其合同关系自动终止，未发生的费用不退。</p>
    <p>（3）由于甲方的过错，使<span>乙方</span>、履行辅助人、旅游从业人员或者其他旅游者遭受损害的，甲方应当赔偿损失。</p>
    <p>（4）甲方在旅游活动中或者在解决纠纷时，应采取措施防止损失扩大，否则应当就扩大的损失承担相应的责任。</p>
    <p>（5）甲方违反安全警示规定，或者对国家应对重大突发事件暂时限制旅游活动的措施、安全防范和应急处置措施不予配合，造成乙方损失的，应当依法承担相应责任。</p>
    <p>（6）由于自身原因导致本合同不能履行，造成甲方人身损害、财产损失的，由其自行承担。</p>
    <p>（7）甲方在自行安排活动期间人身、财产权益受到损害的，乙方在事前已尽到必要警示说明义务且事后已尽到必要救助义务的，由其自行承担。</p>
    <p> 2、乙方违约责任</p>
    <p>（1）乙方不履行合同义务或者履行合同义务不符合约定的，应当依法承担继续履行、采取补救措施或者赔偿损失等违约责任；造成甲方人身损害、财产损失的，应当依法承担赔偿责任。</p>
    <p>（2）乙方具备履行条件，经甲方要求仍拒绝履行合同，<span class="red">乙方向甲方支付旅游费用总额30%的违约金，</span>造成甲方人身损害、滞留等严重后果的，甲方还可以要求乙方支付旅游费用1倍以上3倍以下的赔偿金。</p>
    <p>（3）在甲方自行安排活动期间，乙方未尽到安全提示、救助义务的，应当对甲方的人身损害、财产损失承担相应责任。</p>
    <p>（4）乙方根据甲方的具体要求安排旅游行程，与甲方订立旅游合同的，甲方请求变更旅游行程安排，因此增加的费用由甲方承担，减少的费用退还甲方。</p>
    <p>（5）未经甲方同意，乙方擅自将甲方转团、拼团、转委托的，或是乙方将不同游览行程、不同服务标准、不同价格的旅游者合并同一团队的，应向甲方支付旅游费用总额25%的违约金；甲方解除合同的，乙方还应向未随团出行的旅游者退还全部旅游费用，向已随团出行的旅游者退还尚未发生的旅游费用。</p>
    <p>（6）在同一旅游行程中，乙方提供相同服务，因甲方的年龄、职业等差异增收费用的，乙方应返还增收的费用。</p>
    <p>（7）因乙方原因造成甲方未能乘坐预定的公共交通工具的，乙方应赔偿甲方的直接经济损失，并支付直接经济损失20%的违约金。</p>
    <p>（8）乙方安排的旅游项目低于合同约定标准的，乙方应退还甲方合同金额与实际花费的差额，并支付与退还费用同额的违约金。</p>
    <p>（9）因乙方选择的接待旅行社、旅游景区、酒店、餐馆、旅游汽车、娱乐活动经营单位等旅游辅助服务者的原因，造成安排的旅游项目与合同不符，乙方应退还甲方合同金额与实际花费的差额，并支付与退还费用同额的违约金。</p>
    <p>（10）乙方委派的导游人员未按照国家、旅游行业或地方标准提供导游服务，乙方应向甲方支付旅游费用总额3%的违约金；委派的导游人员达不到约定等级的，乙方应向甲方支付旅游费用总额5%的违约金；非因不可抗力及特殊原因更换导游人员不通知<span class="red">甲方</span>的，乙方应向甲方支付旅游费用总额2%的违约金，若更换导游后降低星级标准的，按前款处理。</p>
    <p>（11）乙方及其委派的导游人员违反合同约定，乙方应按下述标准承担责任：</p>
    <p>①擅自增加旅游消费项目，费用由乙方承担。</p>
    <p>②擅自缩短游览时间、遗漏旅游景点、减少旅游服务项目的，乙方应退还未完成约定旅游服务项目的费用，并支付与退还费用同额的违约金；遗漏无门票景点的，每遗漏一处乙方向甲方支付旅游费用总额5%的违约金。</p>
    <p>③擅自中止对甲方提供住宿、用餐、交通等旅游服务的，应当负担甲方在被中止旅游服务期间所订的同等级别的住宿、用餐、交通等必要费用，并向甲方支付旅游费用总额30%的违约金。</p>

    <h3>第十一条 减轻或免除责任</h3>
    <p>1、乙方能够证明在旅游质量问题发生之前已采取以下措施的，应减轻或免除责任：</p>
    <p>（1）旅游质量和安全状况已给予充分说明、提醒、劝诫、警告或事先说明。</p>
    <p>（2）所发生的违约问题非乙方责任或无法预知或已采取了预防性措施。</p>
    <p>（3）质量问题的发生全部或部分是由于甲方自身的过错。</p>
    <p>（4）质量问题发生后，乙方及时采取了妥善处理措施。</p>
    <p class="red">2、乙方不履行合同或者履行合同不符合约定时，甲方应配合采取适当措施防止损失的扩大，因防止损失扩大而支出的合理费用，由乙方承担；甲方未配合采取适当措施致使损失扩大的，乙方不应就扩大的损失承担赔偿责任。</p>


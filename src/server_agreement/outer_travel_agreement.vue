    <p>1、乙方将旅游业务委托给境外接待社的，受委托境外接待社必须在国家旅游局推荐的接待社范围内，且须与受委托旅行社签订委托合同，约定接待游客的各项服务安排及标准；受委托旅行社违约的，应承担违约责任；给甲方权益造成损害的，乙方应承担损害赔偿责任；乙方赔偿后，可以向受委托旅行社追偿；受委托旅行社故意或者重大过失造成甲方合法权益损害的，应承担连带责任。</p>
    <p>2、未经甲方同意，乙方不得转团或并团。</p>
    <p  class="red">第六条质量标准</p>
    <p>1、组团社组织旅游活动，应向具有相应资质的合法供应商订购产品和服务。</p>
    <p>2、旅游购物及自费项目需经甲乙双方特别约定。</p>
    <p>3、乙方为甲方委派的领队人员必须持有领队证。</p>
    <p class="red">第七条旅游费用</p>
    <p>1、旅游费用包括：</p>
    <p>（1）乙方代订飞机票（含境外机场税）或火车票等城市间交通费用；（2）旅游观光汽车费用；（3）住宿费；（4）餐费；（5）景区第一道门票费；（6）旅游项目费用；（7）旅行社服务费；（8）领队服务费。</p>
    <p>2、旅游费用不包括：(1)旅游者投保的个人旅游保险费用；(2)合同约定需要旅游者另行付费项目的费用；(3)合同未约定由旅行社支付的费用；(4)行程中发生的旅游者个人费用。</p>
    <p class="red">第八条付款方式</p>

    <p>1、签订合同时，甲方应向乙方支付全额旅游费用。</p>
    <p>2、支付旅游费用时，乙方应向甲方出具发票。</p>
    <p class="red">第九条旅游安全保障</p>
    <p>1、乙方必须购买云南旅游组合保险，并保证所提供的接待服务达到相关旅游安全标准要求。</p>
    <p>2、乙方应当提示旅游者购买旅游人身意外保险及相关保险；旅游者在旅游期间因自身身体状况或者其他意外因素发生人身伤害或财产损失的，按照已购买保险的相关条款处理。</p>
    <p class="red"> 第十条合同履行</p>
    <p>1、甲方的权利义务：</p>
    <p>（1）有权要求乙方按照合同约定的内容和标准，履行包价旅游合同义务；</p>
    <p>（2）有权拒绝乙方未经事先协商一致的转团行为和合同约定以外的购物及自费项目安排；</p>
    <p>（3）遵守合同约定，配合领队、导游人员完成旅游行程；</p>
    <p>（4）遵守我国和旅游目的地国家（地区）的法律法规，尊重旅游目的地国家（地区）的风俗习惯、文化传统和宗教信仰，遵守旅游文明行为规范；</p>
    <p>（5）行程中发生纠纷，应当本着平等协商的原则解决，采取适当措施防止损失的扩大，不得损害当地居民的合法权益，不得干扰他人的旅游活动，不得损害旅游经营者和旅游从业人员的合法权益，不得以拒绝登机（车、船）等行为拖延行程或者脱团。</p>
    <p>2、乙方的权利义务：</p>
    <p>（1）有权拒绝甲方超出合同约定的要求；</p>
    <p>（2）在出团前召开说明会，把根据合同细化的行程表发给甲方，说明所到国家（地区）的重要规定、风俗习惯、境外小费的支付标准、外汇兑换等事项；</p>
    <p>（3）行程中不得强迫或者变相强迫安排旅游者购物、参加自费项目；</p>
    <p>（4）按照合同约定的内容和标准，为甲方提供符合约定的旅游服务；在使用外语的国家（地区），应当能够为甲方提供普通话解说服务；对可能危及甲方人身、财产安全的项目和须注意的问题，应当事前向甲方作出真实说明和明确警示，并采取防止危害发生的措施。</p>
    <p class="red">第十一条合同变更</p>
    <p>1、旅游行程开始前，甲方可将合同中自身的权利义务转让给第三人，乙方非因正当理由不得拒绝，因此增加的费用由甲方和第三人承担。</p>
    <p>2、甲乙双方协商一致，可以书面形式变更合同约定的内容，并由双方签字、盖章确认，或由甲方与乙方委派的导游或经理、计调人员签字确认；由此增加的旅游费用及给对方造成的损失，由变更提出方承担；减少的旅游费用，乙方应当退还甲方。</p>

    <p>3、在出行前遇到不可抗力（指因自然原因和社会原因引起的，如自然灾害、战争、恐怖活动、动乱、骚乱、罢工、突发公共事件、政府行为等不能预见、不能避免并不能克服的客观情况），双方经协商可以取消行程或者延期出行。取消行程的，乙方向甲方退还旅游费用；乙方能够证明已向地接社或者履行辅助人支付且不可退还的费用的，乙方可部分或者全部免除责任。</p>
    <p>4、在行程中因不可抗力导致无法履行或者继续履行合同的，乙方可以在征得甲方50%以上成员同意后，对相应内容予以变更；因情况紧急无法征求意见时，乙方可以决定内容的变更，但应当就作出的决定承担举证责任。</p>
    <p>5、因不可抗力或特殊原因需更换领队的，需及时告知旅游者。</p>
    <p class="red">第十二条合同解除</p>
    <p>1、甲方在行程开始前30日以上提出解除合同的，未办理签证/签注的，乙方应当向甲方退还全部旅游费用；已办理签证/签注的，应当扣除签证/签注费用。</p>
    <p>2、甲方在行程开始前30日以内以及行程中提出解除合同的，乙方在扣除必要的费用以及签证/签注费用费用后，将余款退还甲方。</p>
    <p>（1）行程开始前30日以内解除合同的，必要的费用按下列标准扣除：</p>
    <p>程开始前29日至15日，按旅游费用总额的5%；</p>
    <p>行程开始前14日至7日，按旅游费用总额的20%；</p>
    <p>行程开始前6日至4日，按旅游费用总额的50%；</p>
    <p>行程开始前3日至1日，按旅游费用总额的60%；</p>
    <p>行程开始当日，按旅游费用总额的70%。</p>
    <p>（2）在行程中解除合同的，必要的费用扣除标准为：旅游费用×70%+（旅游费用-旅游费用×70%)÷旅游天数×已经出游的天数。</p>
    <p>（3）如按上述第（1）款或者第（2）款约定比例扣除的必要的费用低于实际发生的费用，旅游者按照实际发生的费用支付，但最高额不应当超过旅游费用总额。</p>
    <p>（4）解除合同的，旅行社扣除必要的费用后，应当在解除合同通知到达日起5个工作日内为旅游者办结退款手续。</p>
    <p>3、甲方有下列情形之一的，乙方可以解除合同：</p>
    <p>（1）患有传染病等疾病，可能危害其他旅游者健康和安全的；</p>
    <p>（2）携带危害公共安全的物品且不同意交有关部门处理的；</p>
    <p>（3）从事违法或者违反社会公德的活动的；</p>
    <p>（4）从事严重影响其他旅游者权益的活动，且不听劝阻、不能制止的；</p>
    <p>（5）法律规定的其他情形。</p>
    <p>因前款情形解除合同的，乙方应在扣除必要的费用后，将余款退还甲方；给乙方造成损失的，甲方应当依法承担赔偿责任。</p>
    <p>4、因乙方原因在出发前30日以内（含第30日）提出解除合同的，应向甲方退还全部旅游费用，并按下列标准向甲方承担违约责任：</p>
    <p>（1）出发前30日至15日，支付旅游费用总额2%的违约金；</p>
    <p>（2）出发前14日至7日，支付旅游费用总额5%的违约金；</p>
    <p>（3）出发前6日至4日，支付旅游费用总额10%的违约金；</p>
    <p>（4）出发前3日至1日，支付旅游费用总额15%的违约金；</p>
    <p>（5）出发当日，支付旅游费用总额20%的违约金。</p>
    <p>5、因不可抗力或者乙方、履行辅助人已尽合理注意义务仍不能避免的事件，影响旅游行程的，按照下列情形处理：</p>
    <p>（1）合同不能继续履行的，甲乙双方均可解除合同。合同不能完全履行的，乙方向甲方作出说明，甲方同意变更的，可以在合理范围内变更合同；甲方不同意变更的，可以解除合同。</p>
    <p>（2）合同解除的，乙方应当在扣除已向地接社或者履行辅助人支付且不可退还的费用后，将余款退还甲方；合同变更的，因此增加的费用由甲方承担，减少的费用退还甲方。</p>

    <p class="red">第二条出境旅游报名须知</p>
    <p>1、旅游者报名应提供有效身份证件，提交的材料应真实、有效。</p>
    <p>2、旅游者购买、接受旅游服务时，有向旅行社如实告知与旅游活动相关的个人健康信息的义务；鉴于旅游者的健康状况，旅行社有收客与否的权利。</p>
    <p>3、未成年人以及六十周岁以上老年人单独参团，其监护人或家属应向旅行社提供书面同意证明。</p>
    <p>4、签订合同时须付清全额旅游费用，合同方可生效，并向旅行社索要发票。</p>
    <p>5、机票的签转、退票、更改，按航空公司规定执行；如因客人的原因造成的机票损失，由旅游者自行承担。</p>
    <p>6、旅行社通常无法控制火车、飞机等交通工具的铺位、座位和乘坐时间，请游客予以理解；组团社对具体出发集合时间、航班信息、住宿设施等不能确定要素的信息，出发前通知旅游者。</p>
    <p>7、旅游者应保存好本合同及相关的证明材料，以便发生争议时主张自己的权利。</p>
    <p>8、旅游者出境旅游应遵守我国及旅游目的地国家（地区）的法律和有关规定，不脱团，不在境外滞留不归，不携带违禁物品出入境。</p>
    <p class="red">第三条旅游安全注意事项</p>
    <p>1、乘车（机、船）</p>
    <p>（1）游客应按安全管理规则乘车（机、船）；就座时礼让残疾人、老年人和未成年人。</p>
    <p>（2）乘坐交通工具时，应带好有效身份证件，认真核对票面信息；如因游客原因不能按时乘坐交通工具的，旅行社不承担责任。</p>
    <p>（3）游客在乘车途中，切勿与司机闲谈，切勿催促司机开快车及违章行驶；不要将头、手伸出窗外，系好安全带，以免发生意外。</p>
    <p>（4）在交通工具临时停靠期间，应服从工作人员安排，请勿远离。</p>
    <p>（5）游客下车游览、就餐、购物或自由活动时，应关好车窗，贵重物品随身携带，避免遗失、被盗。</p>
    <p>2、住宿</p>
    <p>（1）游客入住酒店后，应了解酒店安全须知，熟悉酒店的安全出口、安全通道及安全转移的路线。</p>
    <p>（2）注意检查酒店所配备的用品是否齐全，有无破损；如有不全或破损，请立即向酒店服务员报告；如因酒店条件所限未能配备，请游客小心谨慎，防止发生意外。</p>
    <p>（3）沐浴、洗漱时小心地滑，以免发生意外。</p>
    <p>（4）贵重物品应寄存酒店总台或自行妥善保管。</p>
    <p>（5）不要将自己住宿的酒店、房号告诉陌生人；出门或睡觉前要锁好门、窗。</p>
    <p>（6）发生火灾或地震等紧急情形时，应科学自救，不要搭乘电梯或随意跳楼。</p>
    <p>3、饮食</p>
    <p>（1）在行程中购买、食用食物需注意食品卫生安全。</p>
    <p>（2）不要接受和食用陌生人赠送的香烟、食物、饮品、药品。</p>
    <p>（3）旅游期间要合理饮食；用餐时应注意地滑。</p>
    <p>（4）旅行社不应安排饮用烈性酒；对因游客自行饮酒发生的意外，旅行社不承担责任。</p>
    <p>4、游览</p>
    <p>（1）团队旅行时游客不得擅自离团、脱团；如果特殊原因离团，应征得导游人员同意。</p>
    <p>（2）听从导游的安排，记住集合的时间和地点，认清所乘坐的车型、车牌号；不要迟到，因迟到造成的后果由个人负责。</p>
    <p>（3）经过危险地段不可拥挤；前往险峻处观光时应充分考虑自身的条件是否可行，不要强求和存侥幸心理。</p>
    <p>（4）游客登山或参与活动时应根据自身身体状况进行，避免过度激烈运动以及自身身体无法适应的活动，同时做好防护工作。</p>
    <p>（5）在水上（江河、湖海、水库）游览或活动时，应听从工作人员安排，注意乘船安全，穿戴救生衣。</p>
    <p>（6）乘坐缆车或其他载人观光运载工具时，应服从工作人员安排；遇超载、超员或其他异常时，千万不要乘坐，以防发生危险。</p>
    <p>（7）自由活动时间自行外出，请告知全陪或其他游客，并注意人身及财产安全；不要乘坐无营业资质的交通工具。</p>
    <p>（8）游览期间游客应结伴而行；带未成年人的游客，请认真履行监护责任，不能让未成年人单独行动，并注意安全。</p>
    <p>（9）旅行社应告知、提醒游客谨慎购物和消费、妥善保管好个人物品，谨慎参加骑马、攀岩、滑翔、漂流、潜水、游泳、跳伞、热气球、蹦极等高风险活动；除特殊团队外，旅行社不安排上述活动，若游客坚持参与，风险自担。</p>
    <p>（10）前往少数民族地区旅游，务必尊重当地风俗习惯，以免发生冲突。</p>
    <p>（11）游客在旅途中发生人身或财产意外事故时，按相关规定或保险合同处理，或公安部门查处，旅行社尽力提供必要的协助。</p>
    <p>5、购物娱乐</p>
    <p>（1）不要购买流动推销人员的商品；无意购买时，不要向推销人员问价或还价。</p>
    <p>（2）购物时要细心鉴别商品真伪，并向经营者索取发票。</p>
    <p>（3）不要随商品推销人员到偏僻地方购物或取物。</p>
    <p>（4）购买需托运的物品，请确认托运方式可行后再购买，以免造成损失。</p>
    <p>（5）在拥挤的场所购物或娱乐时，注意保管好钱物及证件。</p>
    <p>（6）不要单独行动，不要前往管理混乱的娱乐场所；应防止诈骗、盗窃或抢劫事故发生。</p>
    <p class="red">第四条报名与成团</p>
    <p></p>

    <p>1、甲方应自备在团队返程日有效期半年以上的护照或通行证报名，自办的签证/签注应确保在出游期间有效；甲方应提供准确的个人信息（含身体健康信息）。</p>

    <p class="red">第一条  合同使用说明</p>
    <p>1、本合同为示范文本，供云南省经营出境旅游业务的组团旅行社（以下简称“组团社”）与旅游者签订包价旅游合同时使用。</p>
    <p>2、组团社应是取得旅行社业务经营许可证、在工商行政管理机关注册登记的旅行社，以及经旅游行政管理部门备案的旅行社分社；经备案服务网点只能以旅行社的名义与旅游者签订旅游合同。</p>
    <p>3、凡旅游者不在旅行社营业场所签订合同，应注意核实旅行社业务代表的身份、授权委托书，以及营业执照、旅行社业务经营许可证副本或旅行社盖章的副本复印件。</p>
    <p>4、组团社签订合同所加盖的印章，应为旅行社印章或合同专用章。</p>
    <p>5、经组团社委托、旅游行政管理部门备案的代理旅行社（简称代理社）与旅游者签订出境旅游组团合同的，可以使用组团社的合同和印章，也可以使用组团社的合同加盖代理社印章，或者使用代理社的合同和印章。</p>
    <p>6、双方当事人应当自愿选择本合同条款所提供的选择项，或是自愿在空格处填写完整内容。</p>


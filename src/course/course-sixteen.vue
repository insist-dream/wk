      <p>塔銮寺位于老挝首都万象市以北的瓦塔銮寺的北面，塔銮距市区5公里，本为瓦塔銮寺的组成部分，但由于它的重要性远远超过此寺，所以人们通常只称塔銮，是老挝的佛教圣地。</p>
      <p>建于1556年的塔銮寺是老挝寺塔中最为宏伟的一座，佛塔全身用真金覆盖，即使是阴天也可以远远地便看见这座金塔。据说佛塔下面曾埋了佛祖释迦牟尼的头发和佛骨，整座佛塔共3层，高45米，宽54米；主塔的底部也是方形，每个朝向都有一座膜拜亭，第2层有30座配塔，第3层则是覆满金箔的塔体。佛塔底座的设计是允许信徒们攀登上去的，每一层都有着通道和楼梯。回廊每边都有85米长，而且供奉着各种佛像。</p>
      <p>塔銮寺在历史上几经沉浮，寺内宝物也曾被洗劫一空，20世纪30年代重建后，成为老挝人民心中最重要的佛塔。</p>
